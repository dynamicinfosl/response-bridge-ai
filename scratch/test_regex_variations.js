
const IPV4_REGEX = /\b(?:25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(?:\.(?:25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}(?:\/\d+)?\b/;

const tests = [
  "172.16.0.5",
  "172.16.0.5/32",
  "172.16.0.5/24",
  "10.0.0.1",
  "ip: 192.168.1.100",
  "192.168.1.100, 10.0.0.1",
  "10.20.30.40/32 ",
  "10.20.30.40/32,",
  "10.20.30.40/32-test",
  "10.20.30.40/32\n",
  " 10.20.30.40/32"
];

for (const t of tests) {
  const match = t.match(IPV4_REGEX);
  console.log(`"${t}" -> ${match ? match[0] : 'null'}`);
}
